int Check_NO(FILE *CH){
	
  char check;
  int nb=0;
   while((check=fgetc(CH))!=EOF)
                {
                        if(check=='\n') nb++;
                        
        }
 
return nb;
}

int Check_D(FILE *CH){
	
  char check;
  int nb=0;
   while((check=fgetc(CH))!='\n')
                {
                        if(check=='\t') nb++;
                        
        }
 
return nb+1;
}
